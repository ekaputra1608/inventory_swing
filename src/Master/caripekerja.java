    private void UpdateJTable(){
        try{
            con=kon.open();
            st=con.createStatement();
            query="select kd_pekerja, nama_pekerja, jenis_pekerja, harga_satuan from tb_pekerja order by nama_pekerja asc";
            ResultSet rs=st.executeQuery(query);
            while(rs.next()){
                String a=rs.getString("kd_pekerja");
                String b=rs.getString("nama_pekerja");
                String c=rs.getString("jenis_pekerja");
                String d=rs.getString("harga_satuan");
                
                String data []={a,b,c,d};
                tabMode.addRow(data);
            }
        }catch(SQLException sqle){
           JOptionPane.showMessageDialog(null,"Failed show data! "+sqle);       
    }
}
    
    
