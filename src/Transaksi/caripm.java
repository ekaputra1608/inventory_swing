    private void UpdateJTable(){
        try{
            con=kon.open();
            st=con.createStatement();
            query="select kd_pm,nama_pm,satuan,jns_pm,harga_pm,stock from tb_master_pm order by nama_pm asc";
            ResultSet rs=st.executeQuery(query);
            while(rs.next()){
                String a=rs.getString("kd_pm");
                String b=rs.getString("nama_pm");
                String c=rs.getString("satuan");
                String d=rs.getString("jns_pm");
                String e=rs.getString("harga_pm");
                String f=rs.getString("stock");
                
                String data []={a,b,c,d,e,f};
                tabMode.addRow(data);
            }
        }catch(SQLException sqle){
           JOptionPane.showMessageDialog(null,"Failed show data! "+sqle);       
    }
}
    
    
